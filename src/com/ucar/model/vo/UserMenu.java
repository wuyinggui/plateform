package com.ucar.model.vo;

/**
 * @author lenovo
 *
 */
public class UserMenu {
	private String user_name;
	private String menu_ids;
	private String city_ids;
	private String time;
	public String getUser_name() {
		return user_name;
	}
	public void setUser_name(String user_name) {
		this.user_name = user_name;
	}
	public String getMenu_ids() {
		return menu_ids;
	}
	public void setMenu_ids(String menu_ids) {
		this.menu_ids = menu_ids;
	}
	public String getCity_ids() {
		return city_ids;
	}
	public void setCity_ids(String city_ids) {
		this.city_ids = city_ids;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
}
